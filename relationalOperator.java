public class relationalOperator {
    public static void main(String[] args) {
        int a = 5;
        int b = 7;
        // Relational operators
        System.out.println(a<b);
        System.out.println(a<=b);
        System.out.println(a>b);
        System.out.println(a>=b);
        System.out.println(a==b);
        System.out.println(a!=b);

    }
}
